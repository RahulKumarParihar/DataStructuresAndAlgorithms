package Tree.BinaryTree.BinarySearchTree;

public final class Balanced {
    private boolean isBalance;
    private boolean isLeftHeavy;

    public boolean isBalance() {
        return isBalance;
    }

    public void setBalance(boolean balance) {
        isBalance = balance;
    }

    public boolean isLeftHeavy() {
        return isLeftHeavy;
    }

    public void setLeftHeavy(boolean leftHeavy) {
        isLeftHeavy = leftHeavy;
    }
}
